package com.qf.pojo;

public class kucun {
    private  int id ;
   private  String name;
   private  String ss;
   private  String oo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSs() {
        return ss;
    }

    public void setSs(String ss) {
        this.ss = ss;
    }

    public String getOo() {
        return oo;
    }

    public void setOo(String oo) {
        this.oo = oo;
    }
}
